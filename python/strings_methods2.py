# count

x = "i love python and love c++"
print(x.count("love"))

x = "i love python and love c++\n"
print(x.count("love",0,25))

# startswith()

x = "my name is Abdelfattah"
print(x.startswith("m"))
print(x.startswith("a"))

x = "my name is Abdelfattah\n"
print(x.startswith("m",5,9))

